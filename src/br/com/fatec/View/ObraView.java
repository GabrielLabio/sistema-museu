    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if(jTObra.getSelectedRow() != -1) {
            Obra o = new Obra();
            ObraDAO dao = new ObraDAO();
            
            o.setTombo((int) jTObra.getValueAt(jTObra.getSelectedRow(), 0));
            
            dao.delete(o);

            limparCampos();
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione a exposição que deseja excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        if(jTObra.getSelectedRow() != -1) {
            Obra o = new Obra();
            ObraDAO dao = new ObraDAO();
            o.setTitulo(txtTitulo.getText());
            o.setAutor(txtAutor.getText());
            o.setDataCriacao(txtDataCriacao.getText());
            o.setDescricao(txtDescricao.getText());
            o.setTombo((int) jTObra.getValueAt(jTObra.getSelectedRow(), 0));
            
            dao.update(o);

            limparCampos();
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione a exposição que deseja atualizar!");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

