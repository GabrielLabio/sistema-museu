    public void limparCampos() {
        txtTitulo.setText("");
        txtAutor.setText("");
        txtDataCriacao.setText("");
        txtDescricao.setText("");
    }
    
    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTObra.getModel();
        modelo.setNumRows(0);
        ObraDAO dao = new ObraDAO();
        for(Obra o: dao.read()) {
            modelo.addRow(new Object[] {
                o.getTombo(),
                o.getTitulo(),
                o.getAutor(),
                o.getDescricao(),
                o.getDataCriacao()
            });
        }
    }
    
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Obra o = new Obra();
        ObraDAO dao = new ObraDAO();
        o.setTitulo(txtTitulo.getText());
        o.setAutor(txtAutor.getText());
        o.setDataCriacao(txtDataCriacao.getText());
        o.setDescricao(txtDescricao.getText());
        o.setEmExposicao("NÃO");
        
        if (
            txtTitulo.getText().isEmpty() ||
                txtAutor.getText().isEmpty() ||
                txtDataCriacao.getText().isEmpty() ||
                txtDescricao.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else {
            dao.create(o);
            limparCampos();
            readJTable();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

