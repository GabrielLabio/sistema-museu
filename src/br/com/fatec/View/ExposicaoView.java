    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        if(jTExposicoes.getSelectedRow() != -1) {
            Exposicao e = new Exposicao();
            ExposicaoDAO dao = new ExposicaoDAO();
            e.setNome(txtNome.getText());
            e.setDataIni(txtDataIni.getText());
            e.setDataFim(txtDataFim.getText());
            e.setHoraIni(txtHoraIni.getText());
            e.setHoraFim(txtHoraFim.getText());
            e.setTipo((String) cmbTipo.getSelectedItem());
            e.setSalas(txtSalas.getText());
            e.setCodigoExpo((int) jTExposicoes.getValueAt(jTExposicoes.getSelectedRow(), 0));
            
            dao.update(e);

            limparCampos();
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione a exposição que deseja atualizar!");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Exposicao e = new Exposicao();
        ExposicaoDAO dao = new ExposicaoDAO();
        e.setNome(txtNome.getText());
        e.setDataIni(txtDataIni.getText());
        e.setDataFim(txtDataFim.getText());
        e.setHoraIni(txtHoraIni.getText());
        e.setHoraFim(txtHoraFim.getText());
        e.setTipo((String) cmbTipo.getSelectedItem());
        e.setSalas(txtSalas.getText());
        
        if (txtNome.getText().isEmpty() ||
                txtDataIni.getText().isEmpty() ||
                //txtDataFim.getText().isEmpty() ||
                txtHoraIni.getText().isEmpty() ||
                txtHoraFim.getText().isEmpty() ||
                cmbTipo.getSelectedItem() == "0" ||
                txtSalas.getText().isEmpty()) {
            
                    JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else {
            dao.create(e);
            limparCampos();
            readJTable();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

