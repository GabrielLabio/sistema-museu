    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTExposicoes.getModel();
        modelo.setNumRows(0);
        ExposicaoDAO dao = new ExposicaoDAO();
        for(Exposicao e: dao.read()) {
            modelo.addRow(new Object[] {
                e.getCodigoExpo(),
                e.getNome(),
                e.getDataIni(),
                e.getDataFim(),
                e.getHoraIni(),
                e.getHoraFim(),
                e.getTipo(),
                e.getSalas()
            });
        }
    }

