package br.com.fatec.Model;

public class Restauracao {
    private int protocoloRest;
    private int tombo;
    private String descricao;
    
    

    public int getProtocoloRest() {
        return protocoloRest;
    }

    public void setProtocoloRest(int protocoloRest) {
        this.protocoloRest = protocoloRest;
    }
    
    public int getTombo() {
        return tombo;
    }

    public void setTombo(int tombo) {
        this.tombo = tombo;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    
    
}
